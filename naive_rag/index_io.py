def l2_normalize_vector(vector: np.ndarray) -> np.ndarray:
    import numpy as np

    vector = np.asarray(vector, dtype=np.float32)
    norm = np.linalg.norm(vector)
    if norm == 0:
        return vector
    return vector / norm
